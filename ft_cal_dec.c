#include "ft_printf.h"

int	ft_cal_dec(long long s)
{
	int	i;

	i = 1;
	if (s < 0)
	{
		s *= -1;
		i++;
	}
	while (s >= 10)
	{
		i++;
		s = s / 10;
	}
	return (i);
}
